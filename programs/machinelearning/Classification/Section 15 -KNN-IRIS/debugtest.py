# -*- coding: utf-8 -*-

def addition(num1,num2):
    result1 = num1 + num2
    return result1

def subtration (num1,num2):
    result2 = num1 - num2
    return result2


def multiplication (num1,num2):
    result3 = num1 * num2
    return result3

def division (num1,num2):
    result4 = num1 / num2
    return result4

def mathematicalOperation(num1,num2):
    addition(num1,num2)
    subtration(num1,num2)
    multiplication(num1,num2)
    division(num1,num2)  

mathematicalOperation(10,5)